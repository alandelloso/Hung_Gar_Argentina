			<h1>Viaje a China 2012</h1>
			<h4 class="author">Autor: Hernán Dell' Oso</h4>
			<p>La Asociación WuHsingChuan, una de las entidades más destacadas y reconocidas en el ámbito de las artes marciales en general y del Kung Fu (WuShu) tradicional en particular, con una trayectoria de más de cuatro décadas, encabezada por el Sifu Neldo Sacomani, en 2012 como es habitual en la institución, viajó a Hong Kong y China Continental, para continuar con el vínculo familiar con sus maestros allí, en la cuna del Kung Fu.</p>
			<p>Tuve oportunidad de formar parte de la delegación y vivir una de las experiencias mas destacables, en el camino del arte, que llevo recorriendo hace ya varios años.</p>
			<figure class="pic">
				<img src="../img/foto-grupal-china-2012.jpg" alt="Foto Grupal"  oncontextmenu='return false' ondragstart='return false' onmousedown='return false'>
				<figcaption>Maestros de la Wu Hsing Chuan en la bahía de Hong Kong</figcaption>
			</figure>
			<p>Dentro de la Asociación WuHsingChuan, cinco son los sistemas boxísticos que se difunden:</p>
			<ul>
				<li>VingTsun</li>
				<li>Hung Gar</li>
				<li>BakSiu Lam</li>
				<li>Tai Chi Chuan</li>
				<li>PaKua Chang</li>
			</ul>
			<p>Es el Hung Gar el que está a mi cargo, con el aval de Sifu Sacomani.</p>
			<p>El Hung Gar que se difunde en la Institución, es del linaje original que llega hasta nosotros desde el famoso maestro y héroe de China Wong FeiHung, quién le transmitiera el sistema a uno de sus más destacados discípulos Lam SaiWing, gracias a quién hoy contamos con una buena documentación de destacados aspectos del Hung Gar.</p>
			<p>Lam Sai Wing, transmitió el sistema a su hijo por adopción el Gran Maestro Lam Cho, quién nos acompañó hasta marzo de este año y falleció a la edad de 103 años, sin dejar de entrenar y transmitir sus conocimientos hasta sus últimos días, entre otros a quién fuera uno de sus destacados y más cercano discípulo, el Maestro Benny Wong.</p>
			<p>En este viaje, esperaba entrenar formas avanzadas del sistema y el Sifu Benny Wong, accedió a transmitírmelas en las condiciones que su maestro le había dejado instrucción de hacerlo, sólo a quienes pertenezcan a la familia marcial, dado la necesidad de demostrar la seriedad y el compromiso para recibir tales enseñanzas.</p>
			<p>Esto fue motivo para que previo a recibir la transmisión de las formas, fuera aceptado como miembro de la familia marcial, a través de la ceremonia bai shi (ceremonia del té), con la que tradicionalmente el maestro acepta al discípulo, para transmitirle el sistema en su totalidad.</p>
			<p>En una íntima pero muy emotiva ceremonia, se llevó a cabo la aceptación por parte del Sifu Benny Wong, de mi persona como su discípulo, asumiendo las responsabilidades que tal aceptación implica, ante la presencia de importantes maestros, que oficiaron de testigos del compromiso asumido, tales como Master Hui Yin Fun, Master Lam Chun Chun (hijo del GGM Lam Cho), Sifu Neldo Sacomani, Sifu Luis Villano y Sifu Fernando Carena.</p>
			<figure class="pic">
				<img src="../img/ceremonia-del-te.jpg" alt="Ceremonia del Té"  oncontextmenu='return false' ondragstart='return false' onmousedown='return false'>
				<figcaption>Bai Shi de Hernán Dell' Oso junto a su Sifu Wong Hon Keung</figcaption>
			</figure>
			<p>Durante la ceremonia, recibí por parte de mi Sifu Benny Wong, varios obsequios muy apreciados por mi, entre ellos algunos de gran importancia dentro de las tradiciones de los clanes marciales.</p>
			<p>Al día siguiente y luego de los felices acontecimientos que se sucedieron casi a modo de bienvenida por parte de nuestros maestros en Hong Kong, iniciamos los entrenamientos, perfectamente organizados por nuestros experimentados anfitriones, Sifu Wong y Sifu Hui.</p>
			<p>Durante toda nuestra estadía en Hong Kong, entrenamos a diario durante la mañana y la tarde, algunas veces con un calor agobiante, pero felices por el cordial trato con los maestros y la calidad de los conocimientos que nos transmitieron a cada momento.</p>
			<p>Por la excelencia de nuestros maestros, se hizo muy agradable y llevadero llegar a los objetivos que teníamos como metas de aprendizaje y perfeccionamiento, ya que nos fueron transmitidos con suma dedicación y precisión todos los detalles “finos” de las formas y demás.</p>
			<figure class="pic">
				<img src="../img/tid-sin-kyun.jpg" alt="Tid Sin Kyun"  oncontextmenu='return false' ondragstart='return false' onmousedown='return false'>
				<figcaption>Tid Sin Kyun</figcaption>
			</figure>
			<p>Una de las formas que me enseñó Sifu Wong, la más avanzada del sistema, que muy pocas personas conocen en detalle, llamada Tid Sin Kyun conocida como cable de acero, trabaja intensamente sobre la respiración y la energía interna, a través de tensión dinámica y la emisión de sonidos, produciendo con su desarrollo y entrenamiento, a lo largo del tiempo, numerosos beneficios en cuanto a la salud y el fortalecimiento.</p>
			<p>A la vez que entrenaba la forma, también fui instruido en cuanto a su transmisión, reservada sólo a estudiantes avanzados y muy comprometidos con el desarrollo del sistema.</p>
			<p>Como parte de la tradición Sifu Benny Wong, me llevó junto a toda la delegación, a visitar la tumba de su Sifu el GGM Lam Cho, donde rendí homenaje, un gran honor y un momento muy emotivo para mí.</p>
			<figure class="pic">
				<img src="../img/lam-cho-tomb.jpg" alt="Tumba de Lam Cho"  oncontextmenu='return false' ondragstart='return false' onmousedown='return false'>
