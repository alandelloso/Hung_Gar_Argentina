			</figure>
			<p>Durante nuestra estadía en Hong Kong, tuvimos oportunidad de conocer a otros maestros amigos de Sifu Wong, a quienes nos presentaron ,nos fotografiamos con algunos de ellos, y hasta mostraron a modo de exhibición, algunas formas de sus sistemas, toda gente muy agradable y reconocida por su trayectoria en el ambiente del kung fu tradicional.</p>
			<p>También compartimos gratos momentos con nuestros maestros, cuando fuimos agasajados por ellos, invitados a disfrutar de alguna comida típica de su cultura.</p>
			<p> Nuestro tiempo en Hong Kong llegaba a su fin, en apariencia para nosotros muy rápidamente, lo que habitualmente pasa cuando se viven tan buenas experiencias y se está tan a gusto.</p>
			<p>Nos despedimos de nuestros maestros, y partimos para Foshan en China Continental, a continuar con el viaje de entrenamiento y visitar la Escuela del Maestro de nuestro Sifu Sacomani, el GGM Lun Kai.</p>
			<p>Estoy muy feliz de haber tenido la posibilidad de concretar esta experiencia y muy agradecido con Sifu Sacomani, quién lo hizo posible gracias a su intachable trayectoria, que nos ha permitido a algunos de sus alumnos, llegar a entrenar en forma directa con estos grandes y reconocidos maestros a nivel mundial, referentes de sus respectivos sistemas boxísticos.</p>
			<p>También quiero expresar mi gratitud con mi Sifu Benny Wong, por su generosidad, paciencia, calidad y calidez humana, al aceptarme como parte de su familia marcial, y pasarme los conocimientos que recibiera de su Sifu el GGM Lam Cho, lo que para mí es invaluable.</p>
